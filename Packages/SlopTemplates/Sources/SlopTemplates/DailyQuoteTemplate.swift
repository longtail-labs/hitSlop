// MARK: - Template (generated by @SlopTemplate macro)

@SlopTemplate(
    id: "com.hitslop.templates.daily-quote",
    name: "Daily Quote",
    description: "Display a favorite quote and attribution in a compact inspirational card.",
    version: "1.0.0",
    width: 360, height: 280,
    shape: .roundedRect(radius: 24),
    alwaysOnTop: true,
    categories: ["personal"]
)
struct DailyQuoteView: View {
    @TemplateData var data: DailyQuoteData
    @Environment(\.slopTheme) private var theme
    @Environment(\.slopRenderTarget) private var renderTarget

    @State private var displayedIndex: Int = 0

    private var todayString: String {
        let formatter = DateFormatter()
        formatter.dateFormat = "yyyy-MM-dd"
        return formatter.string(from: Date())
    }

    var body: some View {
        VStack(spacing: 0) {
            // Header
            HStack {
                SlopTextField("Title", text: $data.title)
                    .font(theme.font(size: 13, weight: .semibold))
                    .foregroundStyle(theme.secondary)

                Spacer()

                SlopInteractiveOnly {
                    Text("\(data.safeIndex + 1)/\(data.quotes.count)")
                        .font(theme.mono(size: 11, weight: .medium))
                        .foregroundStyle(theme.secondary.opacity(0.6))
                }
            }
            .padding(.horizontal, 24)
            .padding(.top, 20)
            .padding(.bottom, 8)

            Divider()
                .background(theme.divider)
                .padding(.horizontal, 24)

            // Quote body
            Spacer()

            if !data.quotes.isEmpty {
                VStack(spacing: 12) {
                    SlopEditable(quoteTextBinding) { value in
                        Text(value)
                            .font(theme.font(size: 18, weight: .medium))
                            .foregroundStyle(theme.foreground)
                            .multilineTextAlignment(.center)
                            .lineSpacing(4)
                    } editor: { binding in
                        TextField("Quote text", text: binding)
                            .font(theme.font(size: 18, weight: .medium))
                            .foregroundStyle(theme.foreground)
                            .multilineTextAlignment(.center)
                            .textFieldStyle(.plain)
                    }
                    .id(displayedIndex)
                    .transition(.asymmetric(
                        insertion: .move(edge: .trailing).combined(with: .opacity),
                        removal: .move(edge: .leading).combined(with: .opacity)
                    ))

                    SlopEditable(quoteAuthorBinding) { value in
                        Text("\u{2014} \(value)")
                            .font(theme.font(size: 13))
                            .foregroundStyle(theme.secondary)
                    } editor: { binding in
                        TextField("Author", text: binding)
                            .font(theme.font(size: 13))
                            .foregroundStyle(theme.secondary)
                            .multilineTextAlignment(.center)
                            .textFieldStyle(.plain)
                    }
                    .id("author-\(displayedIndex)")
                    .transition(.asymmetric(
                        insertion: .move(edge: .trailing).combined(with: .opacity),
                        removal: .move(edge: .leading).combined(with: .opacity)
                    ))
                }
                .padding(.horizontal, 28)
            } else {
                Text("No quotes yet")
                    .font(theme.font(size: 14))
                    .foregroundStyle(theme.secondary)
            }

            Spacer()

            // Navigation (interactive only)
            SlopInteractiveOnly {
                HStack {
                    Button { navigatePrev() } label: {
                        Image(systemName: "chevron.left")
                            .font(theme.font(size: 14, weight: .medium))
                            .foregroundStyle(theme.secondary)
                            .frame(width: 32, height: 32)
                            .contentShape(Rectangle())
                    }
                    .buttonStyle(.plain)
                    .disabled(data.quotes.isEmpty)

                    Spacer()

                    Button {
                        withAnimation(.easeInOut(duration: 0.3)) {
                            var newQuote = Quote()
                            newQuote.text = "New quote"
                            newQuote.author = "Author"
                            data.quotes.append(newQuote)
                            data.currentIndex = Double(data.quotes.count - 1)
                            displayedIndex = data.safeIndex
                        }
                    } label: {
                        Image(systemName: "plus")
                            .font(theme.font(size: 12, weight: .medium))
                            .foregroundStyle(theme.secondary.opacity(0.6))
                    }
                    .buttonStyle(.plain)

                    Spacer()

                    Button { navigateNext() } label: {
                        Image(systemName: "chevron.right")
                            .font(theme.font(size: 14, weight: .medium))
                            .foregroundStyle(theme.secondary)
                            .frame(width: 32, height: 32)
                            .contentShape(Rectangle())
                    }
                    .buttonStyle(.plain)
                    .disabled(data.quotes.isEmpty)
                }
                .padding(.horizontal, 24)
                .padding(.bottom, 16)
            }
        }
        .background(theme.background)
        .onAppear { checkDailyRotation() }
    }

    // MARK: - Quote Bindings

    private var quoteTextBinding: Binding<String> {
        Binding<String>(
            get: { data.quotes.indices.contains(data.safeIndex) ? data.quotes[data.safeIndex].text : "" },
            set: { newValue in
                if data.quotes.indices.contains(data.safeIndex) {
                    data.quotes[data.safeIndex].text = newValue
                }
            }
        )
    }

    private var quoteAuthorBinding: Binding<String> {
        Binding<String>(
            get: { data.quotes.indices.contains(data.safeIndex) ? data.quotes[data.safeIndex].author : "" },
            set: { newValue in
                if data.quotes.indices.contains(data.safeIndex) {
                    data.quotes[data.safeIndex].author = newValue
                }
            }
        )
    }

    // MARK: - Daily Rotation

    private func checkDailyRotation() {
        let today = todayString
        if data.lastShownDate != today, !data.quotes.isEmpty {
            let nextIndex = (Int(data.currentIndex) + 1) % data.quotes.count
            data.currentIndex = Double(nextIndex)
            data.lastShownDate = today
        }
        displayedIndex = data.safeIndex
    }

    // MARK: - Navigation

    private func navigatePrev() {
        guard !data.quotes.isEmpty else { return }
        withAnimation(.easeInOut(duration: 0.3)) {
            let prev = (Int(data.currentIndex) - 1 + data.quotes.count) % data.quotes.count
            data.currentIndex = Double(prev)
            displayedIndex = data.safeIndex
        }
    }

    private func navigateNext() {
        guard !data.quotes.isEmpty else { return }
        withAnimation(.easeInOut(duration: 0.3)) {
            let next = (Int(data.currentIndex) + 1) % data.quotes.count
            data.currentIndex = Double(next)
            displayedIndex = data.safeIndex
        }
    }
}

