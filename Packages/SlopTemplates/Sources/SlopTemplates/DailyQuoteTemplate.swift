import Foundation
import SwiftUI
import SlopKit

// MARK: - Data Model

@SlopData
public struct Quote: Identifiable {
    @Field("ID") public var id: String = UUID().uuidString
    @Field("Text") var text: String = ""
    @Field("Author") var author: String = ""
}

@SlopData
public struct DailyQuoteData {
    @SlopKit.Section("Settings")
    @Field("Title") var title: String = "Daily Quote"
    @Field("Current Index") var currentIndex: Double = 0
    @Field("Last Shown Date") var lastShownDate: String = ""

    @SlopKit.Section("Quotes")
    @Field("Quotes") var quotes: [Quote] = DailyQuoteData.defaultQuotes

    var safeIndex: Int {
        guard !quotes.isEmpty else { return 0 }
        return Int(currentIndex) % quotes.count
    }

    var currentQuote: Quote? {
        guard !quotes.isEmpty else { return nil }
        return quotes[safeIndex]
    }
}

